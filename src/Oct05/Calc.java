package Oct05;

public class Calc {
    public int sum(int x, int y){
        int add = x+y;
        return add;
    }
    public int sub(int x, int y){
        int sub = x-y;
        return sub;
    }
    public int multiplication(int x, int y){
        int mul=x*y;
        return mul;
    }
    public int divide(int x, int y){
        int div=x/y;
        return div;
    }
}
